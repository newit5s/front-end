// Generate 3D points from lat/lng coordinates
export function generateContinentPoints(radius: number): Float32Array {
    const points = new Float32Array(CONTINENT_POINTS.length * 3);

    for (let i = 0; i < CONTINENT_POINTS.length; i++) {
        const [lat, lng] = CONTINENT_POINTS[i];
        const phi = (90 - lat) * (Math.PI / 180);
        const theta = (lng + 180) * (Math.PI / 180);

        points[i * 3] = -(radius * Math.sin(phi) * Math.cos(theta));
        points[i * 3 + 1] = radius * Math.cos(phi);
        points[i * 3 + 2] = radius * Math.sin(phi) * Math.sin(theta);
    }

    return points;
}
